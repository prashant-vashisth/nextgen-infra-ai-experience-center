router.post('/create-pr', async (req, res) => {
  const { resource } = req.body;
  const tf = TF[resource];
  if (!tf) return res.status(400).json({ error: 'Invalid resource type' });

  const ts     = Date.now();
  const branch = tf.branch(ts);
  const owner  = process.env.GITHUB_REPO_OWNER;
  const repo   = process.env.GITHUB_REPO_NAME || 'humana-iac-modules';

  if (!owner || !process.env.GITHUB_TOKEN) {
    await sleep(1400);
    const n = 200 + Math.floor(Math.random() * 400);
    return res.json({ pr: { number: n, title: tf.title, html_url: `https://github.com/humana-platform/${repo}/pull/${n}`, state: 'open', created_at: new Date().toISOString() }, branch, mode: 'demo' });
  }

  try {
    const { data: ref } = await axios.get(`https://api.github.com/repos/${owner}/${repo}/git/ref/heads/main`, { headers: ghHeaders() });
    await axios.post(`https://api.github.com/repos/${owner}/${repo}/git/refs`, { ref: `refs/heads/${branch}`, sha: ref.object.sha }, { headers: ghHeaders() });
    const ex = await axios.get(`https://api.github.com/repos/${owner}/${repo}/contents/${tf.path}`, { headers: ghHeaders(), params: { ref: 'main' } }).catch(() => null);
    await axios.put(`https://api.github.com/repos/${owner}/${repo}/contents/${tf.path}`, {
      message: `[CAPE] ${tf.path} — AI rightsizing`,
      content: Buffer.from(tf.content).toString('base64'),
      branch,
      ...(ex?.data?.sha ? { sha: ex.data.sha } : {}),
    }, { headers: ghHeaders() });
    const { data: pr } = await axios.post(`https://api.github.com/repos/${owner}/${repo}/pulls`, { title: tf.title, body: tf.body, head: branch, base: 'main' }, { headers: ghHeaders() });
    res.json({ pr, branch, mode: 'live' });
  } catch (e) {
    const n = 200 + Math.floor(Math.random() * 400);
    res.json({ pr: { number: n, title: tf.title, html_url: `https://github.com/humana-platform/${repo}/pull/${n}`, state: 'open', created_at: new Date().toISOString() }, branch, mode: 'demo', error: e.message });
  }
});

