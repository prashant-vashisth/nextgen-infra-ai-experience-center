router.get('/azure-status', async (req, res) => {
  const tok = await getAzureToken();
  const c   = azCfg();
  const SQLAPI = '2021-11-01';
  const STAPI  = '2021-09-01';

  const portalBase = `https://portal.azure.com/#resource/subscriptions/${c.sub}/resourceGroups/${c.rg}/providers`;

  if (!tok || !c.sub) {
    return res.json({
      mode: 'demo',
      sql:  { found: false, name: c.sqlDb,  message: 'Run setup script to provision live resources' },
      blob: { found: false, name: c.storage, message: 'Run setup script to provision live resources' },
    });
  }

  const http = armClient(tok);
  const result = { mode: 'live', sql: null, blob: null };

  // SQL Database
  try {
    const r = await http.get(`/subscriptions/${c.sub}/resourceGroups/${c.rg}/providers/Microsoft.Sql/servers/${c.sqlSrv}/databases/${c.sqlDb}?api-version=${SQLAPI}`);
    if (r.status === 200) {
      const d = r.data;
      result.sql = {
        found: true,
        name: d.name,
        id: d.id,
        portalUrl: `${portalBase}/Microsoft.Sql/servers/${c.sqlSrv}/databases/${c.sqlDb}/overview`,
        server: c.sqlSrv,
        location: d.location,
        currentSku: d.sku?.name,
        maxVCores: d.sku?.capacity,
        minVCores: d.properties?.minCapacity,
        autoPauseDelay: d.properties?.autoPauseDelay,
        status: d.properties?.status,
        tier: d.sku?.tier,
        resourceGroup: c.rg,
      };
    } else {
      result.sql = { found: false, name: c.sqlDb, message: 'Resource not found — run setup script' };
    }
  } catch (e) {
    result.sql = { found: false, name: c.sqlDb, message: e.message };
  }

  // Storage Account + lifecycle policy
  try {
    const r = await http.get(`/subscriptions/${c.sub}/resourceGroups/${c.rg}/providers/Microsoft.Storage/storageAccounts/${c.storage}?api-version=${STAPI}`);
    if (r.status === 200) {
      const sa = r.data;
      const pol = await http.get(`/subscriptions/${c.sub}/resourceGroups/${c.rg}/providers/Microsoft.Storage/storageAccounts/${c.storage}/managementPolicies/default?api-version=${STAPI}`);
      result.blob = {
        found: true,
        name: sa.name,
        id: sa.id,
        portalUrl: `${portalBase}/Microsoft.Storage/storageAccounts/${c.storage}/overview`,
        location: sa.location,
        tier: sa.properties?.accessTier,
        sku: sa.sku?.name,
        hasLifecyclePolicy: pol.status === 200,
        lifecycleRules: pol.status === 200 ? pol.data?.properties?.policy?.rules : null,
        resourceGroup: c.rg,
      };
    } else {
      result.blob = { found: false, name: c.storage, message: 'Resource not found — run setup script' };
    }
  } catch (e) {
    result.blob = { found: false, name: c.storage, message: e.message };
  }

  res.json(result);
});

